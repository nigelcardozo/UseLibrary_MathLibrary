public class MathX {

    public MathX(){

    }

    public int addX(int x, int y)
    {
        return (x+y);
    }

    public int subtractX(int x, int y)
    {
        return (x-y);
    }

    public int multiplyX(int x, int y)
    {
        return (x*y);
    }

}
